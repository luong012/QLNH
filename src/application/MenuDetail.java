package application;

public class MenuDetail {

	private int menuDetailResourceID;
	private String menuDetailResourceName;
	private int menuDetailResourceQuantity;
	private float menuDetailResourceUnitPrice;
	private String menuDetailResourceUnit;
	private float menuDetailResourcePrice;
	public int getMenuDetailResourceID() {
		return menuDetailResourceID;
	}
	public void setMenuDetailResourceID(int menuDetailResourceID) {
		this.menuDetailResourceID = menuDetailResourceID;
	}
	public String getMenuDetailResourceName() {
		return menuDetailResourceName;
	}
	public void setMenuDetailResourceName(String menuDetailResourceName) {
		this.menuDetailResourceName = menuDetailResourceName;
	}
	public int getMenuDetailResourceQuantity() {
		return menuDetailResourceQuantity;
	}
	public void setMenuDetailResourceQuantity(int menuDetailResourceQuantity) {
		this.menuDetailResourceQuantity = menuDetailResourceQuantity;
	}
	public float getMenuDetailResourceUnitPrice() {
		return menuDetailResourceUnitPrice;
	}
	public void setMenuDetailResourceUnitPrice(float menuDetailResourceUnitPrice) {
		this.menuDetailResourceUnitPrice = menuDetailResourceUnitPrice;
	}
	public String getMenuDetailResourceUnit() {
		return menuDetailResourceUnit;
	}
	public void setMenuDetailResourceUnit(String menuDetailResourceUnit) {
		this.menuDetailResourceUnit = menuDetailResourceUnit;
	}
	public float getMenuDetailResourcePrice() {
		return menuDetailResourcePrice;
	}
	public void setMenuDetailResourcePrice(float menuDetailResourcePrice) {
		this.menuDetailResourcePrice = menuDetailResourcePrice;
	}
}
