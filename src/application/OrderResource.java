package application;

public class OrderResource {
	

	private int orderResourceID;
	private String orderResourceName;
	private int orderResourceQuantity;
	private float orderResourceUnitPrice;
	private String orderResourceUnit;
	private float orderResourcePrice;
	public int getOrderResourceID() {
		return orderResourceID;
	}
	public void setOrderResourceID(int orderResourceID) {
		this.orderResourceID = orderResourceID;
	}
	public String getOrderResourceName() {
		return orderResourceName;
	}
	public void setOrderResourceName(String orderResourceName) {
		this.orderResourceName = orderResourceName;
	}
	public int getOrderResourceQuantity() {
		return orderResourceQuantity;
	}
	public void setOrderResourceQuantity(int orderResourceQuantity) {
		this.orderResourceQuantity = orderResourceQuantity;
	}
	public float getOrderResourceUnitPrice() {
		return orderResourceUnitPrice;
	}
	public void setOrderResourceUnitPrice(float orderResourceUnitPrice) {
		this.orderResourceUnitPrice = orderResourceUnitPrice;
	}
	public float getOrderResourcePrice() {
		return orderResourcePrice;
	}
	public void setOrderResourcePrice(float orderResourcePrice) {
		this.orderResourcePrice = orderResourcePrice;
	}
	public String getOrderResourceUnit() {
		return orderResourceUnit;
	}
	public void setOrderResourceUnit(String orderResourceUnit) {
		this.orderResourceUnit = orderResourceUnit;
	}


}
